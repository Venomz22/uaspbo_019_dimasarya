    private void tampilkanData(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Merk Baju");
        model.addColumn("Harga Baju");
        model.addColumn("Detail Pakaian");
        model.addColumn("Jenis Baju");
        model.addColumn("Gender");
        model.addColumn("Umur disarankan");
        model.addColumn("Ukuran Baju");
        
        
        String cari = txtCari.getText();
        try{
            String sql = "SELECT * FROM baju WHERE MerkBaju LIKE '%"+cari+"%' ";
            conn = Config.getConnection();
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while(rs.next()){
                model.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8),});
            }
            TabelSepatu.setModel(model);
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }
 
