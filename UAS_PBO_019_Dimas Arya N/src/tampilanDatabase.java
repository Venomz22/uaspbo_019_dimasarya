    private void TabelSepatuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelSepatuMouseClicked
        int row = TabelSepatu.rowAtPoint(evt.getPoint());
        String idSepatu  = TabelSepatu.getValueAt(row, 0).toString();
        lblidSepatu.setText(idSepatu);
        String NamaSepatu  = TabelSepatu.getValueAt(row, 1).toString();
        txtNamaSepatu.setText(NamaSepatu);
        String HargaSepatu  = TabelSepatu.getValueAt(row, 2).toString();
        txtHargaSepatu.setText(HargaSepatu);
        String Keterangan  = TabelSepatu.getValueAt(row, 3).toString();
        txtKeterangan.setText(Keterangan);
        String JenisSepatu  = TabelSepatu.getValueAt(row, 4).toString();
        CBJenisSepatu.setSelectedItem(JenisSepatu);
        String GenderSepatu = TabelSepatu.getValueAt(row, 5).toString();
        CBGenderSepatu.setSelectedItem(GenderSepatu);
        String UkuranSepatu = TabelSepatu.getValueAt(row, 6).toString();
        CBUkuranSepatu.setSelectedItem(UkuranSepatu);
        String WarnaSepatu = TabelSepatu.getValueAt(row, 7).toString();
        switch (WarnaSepatu) {
            case "Hitam" -> RBWarna1.setSelected(true);
            case "Putih" -> RBWarna2.setSelected(true);
            case "Coklat" -> RBWarna3.setSelected(true);
            case "Biru" -> RBWarna4.setSelected(true);
            default -> {
            }
        }
    }//GEN-LAST:event_TabelSepatuMouseClicked

